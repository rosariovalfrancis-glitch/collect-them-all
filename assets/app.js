function attachActions() {
  document.addEventListener("click", (event) => {
    const addButton = event.target.closest("[data-add]");
    const detailAdd = event.target.closest("[data-detail-add]");
    const removeButton = event.target.closest("[data-remove]");
    const decBtn = event.target.closest("[data-qty-dec]");
    const incBtn = event.target.closest("[data-qty-inc]");
    if (addButton && !addButton.disabled) addToCart(addButton.dataset.add);
    if (detailAdd && !detailAdd.disabled) {
      const target = document.querySelector("[data-card-detail]");
      if (!target) return addToCart(detailAdd.dataset.detailAdd);
      const qtyInput = target.querySelector("[data-detail-qty]");
      const typeBtn = target.querySelector("[data-type-toggle] .type-btn.active");
      const qty = qtyInput ? parseInt(qtyInput.value) || 1 : 1;
      const purchaseType = typeBtn ? typeBtn.dataset.type : "unit";
      addToCart(detailAdd.dataset.detailAdd, { qty, purchaseType });
    }
    if (removeButton) removeFromCart(removeButton.dataset.remove);
    if (decBtn) updateCartQty(decBtn.dataset.qtyDec, decBtn.dataset.purchase, -1);
    if (incBtn) updateCartQty(incBtn.dataset.qtyInc, incBtn.dataset.purchase, 1);
  });
  document.addEventListener("change", (event) => {
    const input = event.target.closest("[data-qty-input]");
    if (!input) return;
    const val = parseInt(input.value);
    if (!val || val < 1) { input.value = 1; }
    const cart = getCart();
    const item = cart.find((entry) => entry.id === input.dataset.qtyInput && entry.purchaseType === input.dataset.purchase);
    if (item) { item.qty = Math.max(1, val || 1); saveCart(cart); renderCartPage(); }
  });
}

