async function fetchSetCards(apiSetId, imagePrefix, cardCount) {
  const cacheKey = `setCards_v16_${apiSetId}`;
  const cached = localStorage.getItem(cacheKey);
  if (cached) {
    try {
      const parsed = JSON.parse(cached);
      if (parsed._cachedAt && Date.now() - parsed._cachedAt < 86400000 && parsed.cards.length > 0) {
        console.log("fetchSetCards using cache: "+parsed.cards.length+" cards for "+apiSetId);
        return parsed.cards;
      }
    } catch {}
  }
  let combined = [];
  try {
    const pageSize = 250;
    let page = 1;
    let totalCount = 0;
    while (true) {
      const res = await Promise.race([
        fetch(`https://api.pokemontcg.io/v2/cards?q=set.id:${apiSetId}&page=${page}&pageSize=${pageSize}&orderBy=number`),
        new Promise((_, reject) => setTimeout(() => reject(new Error("fetch timeout")), 15000))
      ]);
      if (!res.ok) { console.warn("fetchSetCards page "+page+" failed", res.status); break; }
      const json = await Promise.race([
        res.json(),
        new Promise((_, reject) => setTimeout(() => reject(new Error("json timeout")), 15000))
      ]);
      totalCount = json.totalCount || 0;
      const apiCards = (json.data || []);
      combined.push(...apiCards.map((c) => ({
        id: c.id, name: c.name, number: c.number,
        supertype: c.supertype || "", subtypes: c.subtypes || [],
        types: c.types || [], rarity: c.rarity || "",
        image: c.images?.small || c.images?.large || "",
        imageLarge: c.images?.large || c.images?.small || ""
      })));
      console.log("fetchSetCards page "+page+": got "+apiCards.length+" (total "+totalCount+", combined "+combined.length+")");
      if (combined.length >= totalCount) break;
      page++;
    }
  } catch (e) { console.error("fetchSetCards error", e); }
  combined.sort((a, b) => parseInt(a.number) - parseInt(b.number));
  console.log("fetchSetCards done: "+combined.length+" cards for "+apiSetId);
  if (combined.length) {
    localStorage.setItem(cacheKey, JSON.stringify({ _cachedAt: Date.now(), cards: combined }));
  } else {
    // API failed � try returning stale cache
    if (cached) { try { return JSON.parse(cached).cards; } catch {} }
  }
  return combined;
}

