function cartRows() {
  return getCart().map((entry) => {
    const product = products.find((item) => item.id === entry.id);
    if (!product) return { ...entry, product: null, lineTotal: 0 };
    const purchaseType = entry.purchaseType || "unit";
    const isCase = purchaseType === "case";
    const unitPrice = isCase ? product.price : (product.price / (product.caseSize || 1));
    return { ...entry, product, purchaseType, unitPrice, lineTotal: unitPrice * entry.qty };
  });
}

