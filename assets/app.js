/* ─── roundRect helper ─── */
function roundRect(ctx, x, y, w, h, r) {
  ctx.beginPath();
  ctx.moveTo(x + r, y);
  ctx.lineTo(x + w - r, y);
  ctx.quadraticCurveTo(x + w, y, x + w, y + r);
  ctx.lineTo(x + w, y + h - r);
  ctx.quadraticCurveTo(x + w, y + h, x + w - r, y + h);
  ctx.lineTo(x + r, y + h);
  ctx.quadraticCurveTo(x, y + h, x, y + h - r);
  ctx.lineTo(x, y + r);
  ctx.quadraticCurveTo(x, y, x + r, y);
  ctx.closePath();
}

function downloadReceiptForOrder(order) {
  // Convert order.items (stored format) to cartRows format
  var cartRows = [];
  if (order.items && order.items.length) {
    cartRows = order.items.map(function (i) {
      return {
        product: { name: i.name },
        qty: i.qty || 0,
        lineTotal: i.lineTotal || 0,
        purchaseType: (i.type || "Unit").toLowerCase() === "case" ? "case" : "unit",
      };
    });
  }
  var details = order.items
    ? order.items.map(function (i) { return i.name + " x" + (i.qty || 0); }).join(", ")
    : order.details || "";
  var addr = [order.barangay, order.apartment, order.city, order.province, order.zip].filter(Boolean).join(", ") || order.deliveryAddress || "";
  downloadReceipt(
    "",
    order.number || "",
    order.customerName || "",
    order.contactNumber || "",
    details,
    order.total || 0,
    addr,
    cartRows
  );
}

function downloadReceipt(message, orderNumber, customerName, contactNumber, details, total, deliveryAddress, cartRows) {
  const canvas = document.createElement("canvas");
  const ctx = canvas.getContext("2d");
  const W = 302;
  const scale = 3;
  const L = 14;
  const R = W - 14;

  const bg = "#0e071a";
  const text = "#f0f0f0";
  const muted = "rgba(255,255,255,0.55)";

  const rows = cartRows && cartRows.length ? cartRows : [];
  const rowCount = Math.max(4, rows.length);
  const rowH = 20;
  const tableH = 18 + rowCount * rowH;

  // compute total height in CSS pixels
  const pad = 12;
  const headH = 40;
  const gap1 = 6;
  const infoH = 44;
  const gap2 = 4;
  const totalH = 30;
  const gap3 = 6;
  const payH = 32;
  const gap4 = 6;
  const footH = 18;
  const H = pad + headH + gap1 + infoH + gap2 + tableH + gap3 + totalH + gap3 + payH + gap4 + footH + pad;

  canvas.width = W * scale;
  canvas.height = H * scale;
  canvas.style.width = W + "px";
  canvas.style.height = H + "px";
  ctx.scale(scale, scale);

  function fmt(n) { return "₱" + Number(n).toFixed(2); }
  function trunc(t, w) {
    while (ctx.measureText(t).width > w && t.length > 2) t = t.slice(0, -1);
    return t.length < ( (cartRows && cartRows.length ? cartRows[0]?.product?.name || "Item" : "Item") + " (C)").length ? t + "\u2026" : t;
  }

  /* ─── Background ─── */
  ctx.fillStyle = bg;
  ctx.fillRect(0, 0, W, H);

  let y = pad;

  /* ─── Header ─── */
  ctx.textAlign = "center";
  ctx.textBaseline = "top";
  ctx.fillStyle = "#fff";
  ctx.font = "bold 14px monospace";
  ctx.fillText("COLLECT THEM ALL", W / 2, y);
  ctx.fillStyle = muted;
  ctx.font = "8px monospace";
  ctx.fillText("Acknowledgement Receipt", W / 2, y + 18);
  y += headH;

  /* ─── Divider ─── */
  ctx.fillStyle = "rgba(232,168,56,0.3)";
  ctx.fillRect(L, y, R - L, 1);
  y += gap1;

  /* ─── Order Info ─── */
  ctx.textBaseline = "top";
  ctx.textAlign = "left";
  ctx.fillStyle = muted;
  ctx.font = "7px monospace";
  ctx.fillText("ORDER #", L, y);
  ctx.fillStyle = text;
  ctx.font = "bold 11px monospace";
  const ordTxt = "#" + orderNumber;
  const ordW = Math.min(R - L - 80, ctx.measureText(ordTxt).width);
  ctx.fillText(ordTxt, L, y + 9);

  ctx.fillStyle = muted;
  ctx.font = "7px monospace";
  ctx.fillText("CUSTOMER", L, y + 24);
  ctx.fillStyle = text;
  ctx.font = "bold 10px monospace";
  const custW = R - L - 80;
  ctx.fillText(trunc(customerName, custW), L, y + 33);

  ctx.textAlign = "right";
  ctx.fillStyle = muted;
  ctx.font = "7px monospace";
  ctx.fillText("DATE", R, y);
  ctx.fillStyle = text;
  ctx.font = "9px monospace";
  const dateStr = new Date().toLocaleDateString("en-PH", { year: "numeric", month: "short", day: "numeric" });
  ctx.fillText(dateStr, R, y + 9);

  y += infoH;

  /* ─── Items Table ─── */
  ctx.textAlign = "left";
  ctx.fillStyle = "rgba(124,58,237,0.2)";
  ctx.fillRect(L, y, R - L, 18);

  ctx.fillStyle = "#fff";
  ctx.font = "bold 7px monospace";
  ctx.textBaseline = "middle";
  ctx.fillText("DESCRIPTION", L + 6, y + 9);
  ctx.textAlign = "center";
  ctx.fillText("QTY", 218, y + 9);
  ctx.textAlign = "right";
  ctx.fillText("AMOUNT", R - 6, y + 9);

  y += 18;

  for (let i = 0; i < rowCount; i++) {
    const ry = y + i * rowH;
    if (i % 2 === 0) ctx.fillStyle = "rgba(124,58,237,0.06)";
    else ctx.fillStyle = "transparent";
    ctx.fillRect(L, ry, R - L, rowH);

    ctx.strokeStyle = "rgba(124,58,237,0.2)";
    ctx.lineWidth = 0.5;
    ctx.beginPath();
    ctx.moveTo(L, ry + rowH);
    ctx.lineTo(R, ry + rowH);
    ctx.stroke();

    if (i < rows.length) {
      const r = rows[i];
      const desc = (r.product ? r.product.name : "Item") + (r.purchaseType === "case" ? " (C)" : " (U)");
      const qty = r.qty || 0;
      const lt = r.lineTotal || 0;

      ctx.fillStyle = text;
      ctx.font = "7px monospace";
      ctx.textAlign = "left";
      ctx.textBaseline = "middle";
      ctx.fillText(trunc(desc, 218 - L - 12), L + 6, ry + rowH / 2);
      ctx.font = "8px monospace";
      ctx.textAlign = "center";
      ctx.fillText(String(qty), 218, ry + rowH / 2);
      ctx.textAlign = "right";
      ctx.fillText(fmt(lt), R - 6, ry + rowH / 2);
    } else {
      ctx.fillStyle = muted;
      ctx.font = "8px monospace";
      ctx.textAlign = "center";
      ctx.fillText("\u2014", 218, ry + rowH / 2);
    }
  }

  y += rowCount * rowH + gap3;

  /* ─── Total ─── */
  ctx.fillStyle = "rgba(124,58,237,0.15)";
  ctx.fillRect(L, y, R - L, 26);

  ctx.fillStyle = "#fff";
  ctx.font = "bold 8px monospace";
  ctx.textAlign = "left";
  ctx.textBaseline = "middle";
  ctx.fillText("Total Amount Paid", L + 8, y + 13);

  const tw = Math.min(R - L - 16, ctx.measureText(fmt(total)).width + 16);
  const tx = R - tw - 6;
  ctx.fillStyle = "#e8a838";
  ctx.fillRect(tx, y + 3, tw, 20);

  ctx.fillStyle = bg;
  ctx.font = "bold 10px monospace";
  ctx.textAlign = "center";
  ctx.fillText(fmt(total), tx + tw / 2, y + 13);

  y += totalH + gap3;

  /* ─── Payment Note ─── */
  const nx = L;
  const nw = R - L;
  ctx.fillStyle = "rgba(232,168,56,0.1)";
  ctx.fillRect(nx, y, nw, 28);

  ctx.fillStyle = "#fff";
  ctx.font = "7px monospace";
  ctx.textAlign = "center";
  ctx.textBaseline = "middle";
  ctx.fillText("Pay via GCash. Send proof + order # on Messenger.", nx + nw / 2, y + 14);

  y += payH + gap4;

  /* ─── Footer ─── */
  ctx.fillStyle = "rgba(232,168,56,0.15)";
  ctx.fillRect(L + 40, y, R - L - 80, 1);
  y += 8;

  ctx.fillStyle = muted;
  ctx.font = "bold 9px monospace";
  ctx.textAlign = "center";
  ctx.fillText("Thank you for ordering!", W / 2, y + 5);

  ctx.textBaseline = "alphabetic";
  canvas.toBlob((blob) => {
    const a = document.createElement("a");
    a.href = URL.createObjectURL(blob);
    a.download = "receipt-" + orderNumber + ".png";
    a.click();
    URL.revokeObjectURL(a.href);
  }, "image/png");
}

