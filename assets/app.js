function renderShop() {
  const target = document.querySelector("[data-shop-products]");
  if (!target) return;

  const isPreOrderPage = window.location.pathname.includes("pre-order");
  const search = document.querySelector("[data-search]");
  const category = document.querySelector("[data-category]");
  const subcategory = document.querySelector("[data-subcategory]");
  const brand = document.querySelector("[data-brand]");
  const setSelect = document.querySelector("[data-set]");
  const sort = document.querySelector("[data-sort]");

  if (isPreOrderPage && category) category.value = "Pre-Order";

  const params = new URLSearchParams(location.search);
  const selectedCategory = params.get("category");
  if (selectedCategory && category) category.value = selectedCategory;
  const selectedSet = params.get("set");

  if (setSelect) {
    const exps = getExpansions() || [];
    exps.forEach((e) => {
      const val = e.productSet || e.name;
      const opt = document.createElement("option");
      opt.value = val;
      opt.textContent = e.name;
      setSelect.appendChild(opt);
    });
    if (selectedSet) setSelect.value = selectedSet;
  }

  function toggleSubFilters() {
    if (!category || !subcategory || !brand) return;
    const isCards = category.value === "Cards";
    subcategory.hidden = !isCards;
    brand.hidden = !(isCards && subcategory.value === "Graded Cards");
    if (!isCards) subcategory.value = "All";
    if (subcategory.value !== "Graded Cards") brand.value = "All";
    if (setSelect) setSelect.hidden = category.value === "Others";
    if (setSelect && category.value === "Others") setSelect.value = "All";
  }

  function updateShopHeader() {
    const header = document.querySelector("[data-shop-header]");
    if (!header || isPreOrderPage || !category) return;
    const title = header.querySelector(".page-title");
    const sub = header.querySelector(".page-sub");
    const cat = category.value;
    const activeSet = setSelect && !setSelect.hidden && setSelect.value !== "All" ? setSelect.value : null;
    if (activeSet) {
      title.textContent = activeSet;
      sub.textContent = `All products from the ${activeSet} expansion.`;
    } else if (cat === "Cards") {
      title.textContent = "Cards";
      sub.textContent = "Raw or graded singles. Filter by brand for graded cards.";
    } else if (cat === "Sealed Products") {
      title.textContent = "Sealed Products";
      sub.textContent = "ETBs, booster boxes, and sealed collections.";
    } else if (cat === "Others") {
      title.textContent = "Others";
      sub.textContent = "Merchandise, accessories, and more.";
    } else {
      title.textContent = "Shop";
      sub.textContent = "Use the filters to browse by category, type, and brand.";
    }
  }

  function applyFilters() {
    const term = search.value.trim().toLowerCase();
    let list = products.filter((product) => {
      const matchesTerm = `${product.name} ${product.set} ${product.category}`.toLowerCase().includes(term);
      const catValue = category ? category.value : "Pre-Order";
      const matchesCategory = catValue === "All" || product.category === catValue;
      const matchesSub = !subcategory || subcategory.hidden || subcategory.value === "All" || product.subCategory === subcategory.value;
      const matchesBrand = !brand || brand.hidden || brand.value === "All" || product.brand === brand.value;
      const matchesSet = !setSelect || setSelect.hidden || setSelect.value === "All" || product.set === setSelect.value;
      if (!isPreOrderPage && product.category === "Pre-Order") return false;
      return matchesTerm && matchesCategory && matchesSub && matchesBrand && matchesSet;
    });

    const catOrder = { "Sealed Products": 0, "Cards": 1, "Others": 2 };
    list = [...list].sort((a, b) => {
      const aClosed = isPreOrderPage && a.category === "Pre-Order" && a.closed ? 1 : 0;
      const bClosed = isPreOrderPage && b.category === "Pre-Order" && b.closed ? 1 : 0;
      if (aClosed !== bClosed) return aClosed - bClosed;
      const aSold = a.status === "Unavailable" ? 1 : 0;
      const bSold = b.status === "Unavailable" ? 1 : 0;
      if (aSold !== bSold) return aSold - bSold;
      if (sort.value === "low") return a.price - b.price;
      if (sort.value === "high") return b.price - a.price;
      if (sort.value === "newest") return products.indexOf(b) - products.indexOf(a);
      return (catOrder[a.category] ?? 2) - (catOrder[b.category] ?? 2);
    });
    target.innerHTML = list.map(productCard).join("");
    if (category) toggleAllocationSection(category.value);
  }

  toggleSubFilters();
  updateShopHeader();

  [search, category, subcategory, brand, setSelect, sort].forEach((control) => {
    if (control) control.addEventListener("input", () => {
      toggleSubFilters();
      updateShopHeader();
      applyFilters();
    });
  });
  applyFilters();
}

