function addToCart(id, opts = {}) {
  const product = products.find((item) => item.id === id);
  if (!product) return;
  if (product.status === "Unavailable") {
    showToast("This item is sold out.");
    return;
  }

  const qty = opts.qty || 1;
  const purchaseType = opts.purchaseType || (product.caseSize && product.caseSize > 1 ? "unit" : "case");

  const cart = getCart();
  const item = cart.find((entry) => entry.id === id && entry.purchaseType === purchaseType);
  if (item) {
    item.qty += qty;
  } else {
    cart.push({ id, qty, purchaseType });
  }
  saveCart(cart);
  const typeLabel = purchaseType === "case" ? "Case" : "Unit";
  showToast(`Added ${qty} ${typeLabel} to cart.`);
}

function removeFromCart(id) {
  saveCart(getCart().filter((entry) => entry.id !== id));
  renderCartPage();
}

function updateCartQty(id, purchaseType, delta) {
  const cart = getCart();
  const item = cart.find((entry) => entry.id === id && entry.purchaseType === purchaseType);
  if (!item) return;
  item.qty = Math.max(1, (item.qty || 1) + delta);
  saveCart(cart);
  renderCartPage();
}

function updateCartCount() {
  const count = getCart().reduce((sum, item) => sum + item.qty, 0);
  document.querySelectorAll("[data-cart-count]").forEach((node) => {
    node.textContent = count;
  });
  document.querySelectorAll("[data-cart-link]").forEach((link) => {
    link.className = count > 0 ? "btn btn-yellow" : "btn btn-outline";
  });
}

