function renderAllocationPlanner() {
  const productSelect = document.querySelector("[data-alloc-product]");
  const unitsInput = document.querySelector("[data-alloc-units]");
  const percentInput = document.querySelector("[data-alloc-percent]");
  const receivedEl = document.querySelector("[data-alloc-received]");
  const valueEl = document.querySelector("[data-alloc-value]");
  const dpEl = document.querySelector("[data-alloc-dp]");
  const noteEl = document.querySelector("[data-alloc-note]");
  const unitLabel = document.querySelector("[data-alloc-unit-label]");
  const fillNote = document.querySelector("[data-alloc-fill-note]");
  const modeBtns = document.querySelectorAll("[data-alloc-mode]");
  if (!productSelect || !unitsInput || !percentInput) return;

  let mode = "unit";

  const preOrders = products.filter((p) => p.category === "Pre-Order" && !p.closed);
  productSelect.innerHTML = preOrders
    .map((p) => `<option value="${p.id}">${p.name}</option>`)
    .join("");

  modeBtns.forEach((btn) => {
    btn.addEventListener("click", () => {
      modeBtns.forEach((b) => b.classList.remove("active"));
      btn.classList.add("active");
      mode = btn.dataset.allocMode;
      const caseSize = getCaseSize();
      if (mode === "case") {
        unitLabel.textContent = "Cases";
        unitsInput.value = Math.ceil(Number(unitsInput.value) / caseSize);
        fillNote.textContent = `Standard Pack: ${caseSize} units per case. Adjust target cases based on your needs.`;
      } else {
        unitLabel.textContent = "Units";
        unitsInput.value = Number(unitsInput.value) * caseSize;
        fillNote.textContent = "Adjust fill rate based on historical allocation cuts (e.g., 50% means you get half of what you order).";
      }
      calculate();
    });
  });

  function getCaseSize() {
    const product = products.find((item) => item.id === productSelect.value);
    return (product && product.caseSize) || 1;
  }

  function calculate() {
    const product = products.find((item) => item.id === productSelect.value) || preOrders[0];
    if (!product) return;
    const caseSize = getCaseSize();
    const rawTarget = Math.max(1, Number(unitsInput.value) || 1);
    const mult = mode === "case" ? rawTarget * caseSize : rawTarget;
    const unitsRequested = mult;
    const allocationPercent = Math.min(100, Math.max(0, Number(percentInput.value) || 0));
    const unitsReceived = Math.max(0, Math.floor(unitsRequested * (allocationPercent / 100)));
    const receivedTotal = unitsReceived * product.price;
    const receivedDp = receivedTotal * DP_RATE;

    if (receivedEl) receivedEl.textContent = `${unitsReceived} PCS`;
    if (valueEl) valueEl.textContent = peso.format(receivedTotal);
    if (dpEl) dpEl.textContent = peso.format(receivedDp);
    if (noteEl) {
      const label = mode === "case" ? "Case(s)" : "Unit(s)";
      noteEl.textContent = `This DP is calculated based on your Target Order of ${rawTarget} ${label}.`;
    }
  }

  productSelect.addEventListener("change", () => {
    const caseSize = getCaseSize();
    if (mode === "case" && caseSize > 1) {
      fillNote.textContent = `Standard Pack: ${caseSize} units per case. Adjust target cases based on your needs.`;
    } else {
      fillNote.textContent = "Adjust fill rate based on historical allocation cuts (e.g., 50% means you get half of what you order).";
    }
    calculate();
  });
  unitsInput.addEventListener("input", calculate);
  percentInput.addEventListener("input", calculate);
  calculate();
}

