function handleCheckout() {
  const form = document.querySelector("[data-checkout-form]");
  if (!form) return;

  // Block checkout entirely if cart is empty
  if (!getCart().length) {
    showToast("Your cart is empty. Redirecting to shop...");
    setTimeout(function () { location.href = "shop.html"; }, 1200);
    form.hidden = true;
    const summaryPanel = document.querySelector(".summary-panel");
    if (summaryPanel) summaryPanel.hidden = true;
    const emptyMsg = document.createElement("div");
    emptyMsg.className = "card empty-state";
    emptyMsg.innerHTML = '<h2>Your cart is empty.</h2><p class="muted">Add some products before checking out.</p><a class="btn btn-primary" href="shop.html" style="margin-top:16px;">Go to Shop</a>';
    const main = document.querySelector(".checkout-main");
    if (main) main.prepend(emptyMsg);
    return;
  }

  // Custom payment dropdown
  const paymentWrap = form.querySelector("[data-payment-select]");
  if (paymentWrap) {
    const trigger = paymentWrap.querySelector(".custom-select-trigger");
    const options = paymentWrap.querySelector(".custom-select-options");
    const label = paymentWrap.querySelector("[data-select-label]");
    const hiddenInput = paymentWrap.querySelector("[name='payment']");

    trigger.addEventListener("click", (e) => {
      e.stopPropagation();
      const isOpen = options.style.display === "block";
      document.querySelectorAll(".custom-select-options").forEach((o) => o.style.display = "none");
      options.style.display = isOpen ? "none" : "block";
    });

    paymentWrap.querySelectorAll(".custom-select-option").forEach((opt) => {
      opt.addEventListener("click", () => {
        const val = opt.dataset.value;
        label.textContent = val;
        hiddenInput.value = val;
        options.style.display = "none";
        // Swap QR — placeholder only; real QR sent on confirmation
        const qrBox = form.querySelector("[data-checkout-qr]");
        if (qrBox) {
          qrBox.innerHTML = `<p style="color:var(--muted);font-size:0.85rem;text-align:center;padding:20px 0;">Payment QR will be sent to you directly after order confirmation.</p>`;
        }
      });
    });

    document.addEventListener("click", () => { options.style.display = "none"; }, false);
  }

  // QR image lightbox
  document.addEventListener("click", (e) => {
    const img = e.target.closest("[data-qr-img]");
    if (img) showQRLightbox(img.src);
  });

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const user = getCurrentUser();
    if (user) {
      const nameField = form.querySelector("[name='full_name']");
      if (nameField && !nameField.value) nameField.value = user.name;
    }
    const rows = cartRows();
    if (!rows.length) {
      showToast("Your cart is empty.");
      return;
    }

    const formData = new FormData(form);
    const customerName = (formData.get("full_name") || "").trim();
    const contactNumber = (formData.get("phone") || "").trim();
    const customerEmail = user ? user.email : (formData.get("email") || "").trim().toLowerCase();
    const paymentMethod = formData.get("payment") || "BPI";
    const province = (formData.get("province") || "").trim();
    const city = (formData.get("city") || "").trim();
    const barangay = (formData.get("barangay") || "").trim();
    const apartment = (formData.get("apartment") || "").trim();
    const zip = (formData.get("zip") || "").trim();
    const notes = (formData.get("notes") || "").trim();

    const deliveryAddress = [barangay, apartment, city + ", " + province, "ZIP " + zip].filter(Boolean).join(", ");

    if (!customerName || !contactNumber) {
      showToast("Please enter your full name and contact number.");
      return;
    }
    if (!province || !city || !barangay || !zip) {
      showToast("Please fill in all required shipping address fields.");
      return;
    }

    const orderNumber = createOrderNumber();
    const items = rows.filter((r) => r.product).map((row) => ({
      name: row.product.name,
      qty: row.qty,
      type: row.purchaseType === "case" ? "Case" : "Unit",
      lineTotal: row.lineTotal || 0
    }));
    const hasPreOrder = rows.some((row) => row.product && row.product.category === "Pre-Order");
    const details = items.map((i) => `${i.name}`).join(", ");
    const subtotal = rows.reduce((sum, row) => sum + row.lineTotal, 0);
    const total = subtotal + 120;
    const saved = JSON.parse(localStorage.getItem("collectOrders") || "[]");
    saved.push({
      number: orderNumber,
      details,
      items,
      hasPreOrder,
      createdAt: new Date().toISOString(),
      status: "Waiting for Payment",
      customerName,
      contactNumber,
      customerEmail,
      paymentMethod,
      deliveryAddress,
      province,
      city,
      barangay,
      apartment,
      zip,
      notes,
      total
    });
    localStorage.setItem("collectOrders", JSON.stringify(saved));
    saveCart([]);
    showCheckoutSuccess(orderNumber, customerName, contactNumber, details, total, deliveryAddress, rows, notes);
    // Send order to database backend (non-blocking — local save already done)
    submitOrderToAPI({
      orderNumber: orderNumber,
      customerName: customerName,
      contactNumber: contactNumber,
      customerEmail: customerEmail,
      paymentMethod: paymentMethod,
      deliveryAddress: deliveryAddress,
      province: province,
      city: city,
      barangay: barangay,
      apartment: apartment,
      zip: zip,
      notes: notes,
      total: total,
      items: items,
      userId: getCurrentUser()?.id || null,
      hasPreOrder: hasPreOrder,
    });
  });
}

