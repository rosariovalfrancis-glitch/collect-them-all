function tickCountdowns() {
  const now = Date.now();
  let expired = false;

  document.querySelectorAll("[data-countdown]").forEach((el) => {
    const target = new Date(el.dataset.countdown);
    if (isNaN(target.getTime())) return;
    const diff = target.getTime() - now;
    const secs = Math.ceil(diff / 1000);
    if (secs <= 0) {
      el.textContent = "Closed";
      el.style.background = "#555";
      el.style.color = "#ccc";
      expired = true;
    } else if (secs <= 60) {
      el.style.display = "";
      el.textContent = secs + "s";
    } else {
      el.style.display = "none";
    }
  });

  document.querySelectorAll("[data-countdown-full]").forEach((el) => {
    const target = new Date(el.dataset.countdownFull);
    if (isNaN(target.getTime())) return;
    const diff = target.getTime() - now;
    const secs = Math.ceil(diff / 1000);
    if (secs <= 0) {
      el.innerHTML = `<span style="color:var(--red);font-weight:800;">Closed</span>`;
      expired = true;
    } else if (secs <= 60) {
      el.textContent = secs + "s";
    } else {
      const days = Math.floor(diff / 86400000);
      const hours = Math.floor((diff % 86400000) / 3600000);
      const mins = Math.floor((diff % 3600000) / 60000);
      const displaySecs = Math.floor((diff % 60000) / 1000);
      if (days > 0) {
        el.innerHTML = `${days}d ${hours}h ${mins}m ${displaySecs}s`;
      } else {
        el.innerHTML = `${hours}h ${mins}m ${displaySecs}s`;
      }
    }
  });

  if (expired && !window.location.pathname.includes("/admin/")) {
    const all = loadProducts();
    let changed = false;
    all.forEach((p) => {
      if (p.category === "Pre-Order" && p.closesAt && !p.closed) {
        if (new Date(p.closesAt).getTime() <= now) {
          p.closed = true;
          changed = true;
        }
      }
    });
    if (changed) {
      saveProducts(all);
      products = loadProducts();
      renderProducts();
      renderShop();
      renderCardDetail();
    }
  }
}

function startCountdowns() {
  tickCountdowns();
  setInterval(tickCountdowns, 1000);
}

