function migrateProducts(stored) {
  const defaults = defaultProducts();
  let changed = false;
  defaults.forEach((dp) => {
    const existing = stored.find((sp) => sp.id === dp.id);
    if (!existing) {
      stored.push(dp);
      changed = true;
    } else {
      if (existing.closesAt === undefined) { existing.closesAt = dp.closesAt || null; changed = true; }
      if (existing.caseSize === undefined) { existing.caseSize = dp.caseSize || 1; changed = true; }
      if (existing.closed === undefined) { existing.closed = false; changed = true; }
    }
  });
  if (changed) {
    saveProducts(stored);
    if (!localStorage.getItem("collectMigrated")) {
      localStorage.setItem("collectMigrated", "1");
      location.reload();
    }
  }
  return stored;
}

function loadProducts() {
  const stored = localStorage.getItem("collectProducts");
  if (stored) {
    try {
      const parsed = JSON.parse(stored);
      const v = parsed._version || 0;
      if (v < 2) return migrateProducts(parsed);
      localStorage.removeItem("collectMigrated");
      return parsed;
    }
    catch { /* fall through */ }
  }
  const defaults = defaultProducts();
  defaults._version = 2;
  localStorage.setItem("collectProducts", JSON.stringify(defaults));
  return defaults;
}

function saveProducts(list) {
  list._version = 2;
  localStorage.setItem("collectProducts", JSON.stringify(list));
}

